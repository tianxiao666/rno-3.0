//object
function MapCityGrid() {
	this._gridId = 0;
	this._minLng = 0;
	this._minLat = 0;
	this._maxLng = 0;
	this._maxLat = 0;
	
	this._pageSize = 100;
	this._currentPage = 1;
	this._totalPageCnt = -1;
	this._totalCnt = -1;
	
	this._cityId = 0;		
	this._isFinished = false;  //是否加载完成
	this._isLoading = false;	//是否正在加载
}

