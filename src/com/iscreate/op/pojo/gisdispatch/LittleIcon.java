public class LittleIcon {

	private long id;
	private String name;
	private String position;// 位置描述
	private String icon;// 图标
	private PicUnitType picUnitType;// 关联的图元类型
    private boolean needShow;//是否需要显示
    
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public PicUnitType getPicUnitType() {
		return picUnitType;
	}

	public void setPicUnitType(PicUnitType picUnitType) {
		this.picUnitType = picUnitType;
	}

	public boolean isNeedShow() {
		return needShow;
	}

	public void setNeedShow(boolean needShow) {
		this.needShow = needShow;
	}
	
	public String getKey(){
		return id+"";
	}

}
