package com.iscreate.op.service.rno.parser.vo;

public class NcsUmtsCellData extends NcsRecord{

	private int recType=56;
	private int length=19;
	private String cellName;
	private int repUndefUmts;
	private int repUmts;
	public int getRecType() {
		return recType;
	}
	public void setRecType(int recType) {
		this.recType = recType;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public String getCellName() {
		return cellName;
	}
	public void setCellName(String cellName) {
		this.cellName = cellName;
	}
	public int getRepUndefUmts() {
		return repUndefUmts;
	}
	public void setRepUndefUmts(int repUndefUmts) {
		this.repUndefUmts = repUndefUmts;
	}
	public int getRepUmts() {
		return repUmts;
	}
	public void setRepUmts(int repUmts) {
		this.repUmts = repUmts;
	}
	@Override
	public String toString() {
		return "NcsUmtsCellData [recType=" + recType + ", length=" + length
				+ ", cellName=" + cellName + ", repUndefUmts=" + repUndefUmts
				+ ", repUmts=" + repUmts + "]";
	}
	
}
